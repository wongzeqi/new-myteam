package priv.wzq.ssm.po;

public class ExOpenid {
	private String name;
	private String schoolnumber;
	private int role;
	private String openid;
	private String qjtype;
	private String qdtype;
	
	public String getOpenid() {
		return openid;
	}
	public void setOpenid(String openid) {
		this.openid = openid;
	}
	public String getQjtype() {
		return qjtype;
	}
	public void setQjtype(String qjtype) {
		this.qjtype = qjtype;
	}
	public String getQdtype() {
		return qdtype;
	}
	public void setQdtype(String qdtype) {
		this.qdtype = qdtype;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSchoolnumber() {
		return schoolnumber;
	}
	public void setSchoolnumber(String schoolnumber) {
		this.schoolnumber = schoolnumber;
	}
	public int getRole() {
		return role;
	}
	public void setRole(int role) {
		this.role = role;
	}
	
	
}
